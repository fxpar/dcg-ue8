CREATE TABLE IF NOT EXISTS `SAISONNIER` (
	`DateDebutMission`	DATE,
	`DateFinMission`	DATE,
	`Prime`	NUMERIC,
	`CodeEmpSais`	INTEGER,
	PRIMARY KEY(`CodeEmpSais`)
);
CREATE TABLE IF NOT EXISTS `SECTEUR` (
	`NumSecteur`	INTEGER AUTO_INCREMENT,
	`NomSecteur`	TEXT,
	PRIMARY KEY(`NumSecteur` )
);
CREATE TABLE IF NOT EXISTS `TYPE` (
	`CodeType`	INTEGER AUTO_INCREMENT,
	`LibelléType`	TEXT,
	`TarifSemaine`	NUMERIC,
	`TarifNuitée`	NUMERIC,
	PRIMARY KEY(`CodeType` )
);
CREATE TABLE IF NOT EXISTS `HOTEL` (
	`NumHotel`	INTEGER AUTO_INCREMENT,
	`NomHotel`	TEXT,
	`NbEtoiles`	NUMERIC,
	`RueHotel`	TEXT,
	`CPHotel`	TEXT,
	`VilleHotel`	TEXT,
	`PaysHotel`	TEXT,
	PRIMARY KEY(`NumHotel` )
);
CREATE TABLE IF NOT EXISTS `CONCERNER` (
	`NumSecteur`	INTEGER,
	`NumHotel`	INTEGER,
	`CodeEmp`	INTEGER,
	PRIMARY KEY(`NumSecteur`,`NumHotel`),
	FOREIGN KEY(`NumHotel`) REFERENCES `HOTEL`(`NumHotel`),
	FOREIGN KEY(`NumSecteur`) REFERENCES `SECTEUR`(`NumSecteur`)
);

CREATE TABLE IF NOT EXISTS `EMPLOYE` (
	`CodeEmp`	INTEGER AUTO_INCREMENT,
	`NomEmp`	TEXT,
	`PrénomEmp`	TEXT,
	`RueEmp`	TEXT,
	`VilleEmp`	TEXT,
	`CodePostalEmp`	TEXT,
	`Salaire`	NUMERIC,
	`Saisonnier`	TEXT,
	`Permanent`	TEXT,
	`NumSecteur`	INTEGER,
	`NumHotel`	INTEGER,
	CHECK((`Saisonnier` IS NULL AND `Permanent` IS NOT NULL) OR (`Saisonnier` IS NOT NULL AND `Permanent` IS NULL)),
	PRIMARY KEY(`CodeEmp` ),
	FOREIGN KEY(`NumSecteur`) REFERENCES `SECTEUR`(`NumSecteur`),
	FOREIGN KEY(`NumHotel`) REFERENCES `HOTEL`(`NumHotel`)
);
CREATE TABLE IF NOT EXISTS `LODGE` (
	`NumLodge`	INTEGER AUTO_INCREMENT,
	`NomLodge`	TEXT,
	`CapaciteAccueil`	INTEGER,
	`NbPieces`	INTEGER,
	`NbLitsSimples`	INTEGER,
	`NbLitsDoubles`	INTEGER,
	`NumHotel`	INTEGER,
	`CodeType`	INTEGER,
	PRIMARY KEY(`NumLodge` ),
	FOREIGN KEY(`CodeType`) REFERENCES `TYPE`(`CodeType`),
	FOREIGN KEY(`NumHotel`) REFERENCES `HOTEL`(`NumHotel`)
);


